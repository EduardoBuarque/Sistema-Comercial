    private void deleteDetailButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteDetailButtonActionPerformed
        int index = masterTable.getSelectedRow();
        br.com.siscom.beans.CotCab C = list.get(masterTable.convertRowIndexToModel(index));
        Collection<br.com.siscom.beans.CotDet> cs = C.getCotDetList();
        int[] selected = detailTable.getSelectedRows();
        List<br.com.siscom.beans.CotDet> toRemove = new ArrayList<br.com.siscom.beans.CotDet>(selected.length);
        for (int idx = 0; idx < selected.length; idx++) {
            selected[idx] = detailTable.convertRowIndexToModel(selected[idx]);
            int count = 0;
            Iterator<br.com.siscom.beans.CotDet> iter = cs.iterator();
            while (count++ < selected[idx]) {
                iter.next();
            }
            br.com.siscom.beans.CotDet c = iter.next();
            toRemove.add(c);
            entityManager.remove(c);
        }
        cs.removeAll(toRemove);
        masterTable.clearSelection();
        masterTable.setRowSelectionInterval(index, index);
    }//GEN-LAST:event_deleteDetailButtonActionPerformed

    private void newDetailButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_newDetailButtonActionPerformed
        
        entityManager.getTransaction().commit();
        entityManager.getTransaction().begin();
        
        int index = masterTable.getSelectedRow();
        br.com.siscom.beans.CotCab C = list.get(masterTable.convertRowIndexToModel(index));
        Collection<br.com.siscom.beans.CotDet> cs = C.getCotDetList();
        if (cs == null) {
            cs = new LinkedList<br.com.siscom.beans.CotDet>();
            C.setCotDetList((List) cs);
        }
        br.com.siscom.beans.CotDet c = new br.com.siscom.beans.CotDet();
        
        CotDetPK cpk = new CotDetPK();

        cpk.setCodCotCab(C.getCodCotCab());
        c.setCotDetPK(cpk);
        
        c.setCotCab(C);
        ImportaRequisicao ir = new ImportaRequisicao(null, true);
        ir.recebeObjeto(c);
        ir.setVisible(true);
        
     /* produtoQuery = entityManager.createNamedQuery("Produto.findByCodProduto");
        produtoQuery.setParameter("codProduto", c.getCotDetPK().getCodProduto());
        List<Produto> data = produtoQuery.getResultList();
        c.setDescricaoProduto(data.get(2).getDescricaoProduto());*/
        
        reqDetQuery = entityManager.createNamedQuery("ReqDet.findByQtdeReqDet");
        reqDetQuery.setParameter("qtdeReqDet", c.getQuantidadeReq());
        List<ReqDet> data = reqDetQuery.getResultList();
        c.setQuantidadeReq(data.get(0).getQtdeReqDet());
        
        reqDetQuery = entityManager.createNamedQuery("ReqDet.findByVlrTotalReqDet");
        reqDetQuery.setParameter("vlrTotalReqDet", c.getVlrTotal());
        data = reqDetQuery.getResultList();
        c.setQuantidadeReq(data.get(0).getQtdeReqDet());
        
        entityManager.persist(c);      
        cs.add(c);
        masterTable.clearSelection();
        masterTable.setRowSelectionInterval(index, index);
        int row = cs.size() - 1;
        detailTable.setRowSelectionInterval(row, row);
        detailTable.scrollRectToVisible(detailTable.getCellRect(row, 0, true));
    }//GEN-LAST:event_newDetailButtonActionPerformed
    
